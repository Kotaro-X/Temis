const SLOT_LABELS_BY_LANGUAGE: Record<AppLanguage, Record<SlotKey, string>> = {
  ja: {
    morning: "朝",
    forenoon: "午前",
    afternoon: "午後",
    night: "夜",
  },
  en: {
    morning: "Morning",
    forenoon: "Forenoon",
    afternoon: "Afternoon",
    night: "Night",
  },
};

export const t = (language: AppLanguage, key: string): string =>
  MESSAGES[language][key] ?? MESSAGES.ja[key] ?? key;

export const tf = (
  language: AppLanguage,
  key: string,
  vars: Record<string, string | number>,
): string => {
  let value = t(language, key);
  for (const [name, replacement] of Object.entries(vars)) {
    value = value.replace(new RegExp(`\\{${name}\\}`, "g"), String(replacement));
  }
  return value;
};

export const getSlotLabel = (language: AppLanguage, slotKey: SlotKey): string =>
  SLOT_LABELS_BY_LANGUAGE[language][slotKey];
